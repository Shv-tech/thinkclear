'use client';

import { useEffect, useState } from 'react';
import { motion } from 'framer-motion';
import { ANIMATION } from '@/lib/constants';

// Dark/light theme toggle with smooth CSS variable transitions
// Dark mode is default

export default function ThemeToggle() {
    const [theme, setTheme] = useState<'dark' | 'light'>('dark');
    const [mounted, setMounted] = useState(false);

    // Prevent hydration mismatch
    useEffect(() => {
        setMounted(true);

        // Check saved preference or system preference
        const saved = localStorage.getItem('theme');
        if (saved === 'light' || saved === 'dark') {
            setTheme(saved);
        } else {
            // Default to dark (brand preference)
            setTheme('dark');
        }
    }, []);

    // Apply theme to document
    useEffect(() => {
        if (!mounted) return;

        document.documentElement.setAttribute('data-theme', theme);
        localStorage.setItem('theme', theme);
    }, [theme, mounted]);

    const toggleTheme = () => {
        setTheme(prev => prev === 'dark' ? 'light' : 'dark');
    };

    if (!mounted) {
        // Render placeholder to prevent layout shift
        return (
            <button
                type="button"
                aria-label="Toggle theme"
                style={{
                    width: '44px',
                    height: '44px',
                    background: 'transparent',
                    border: 'none',
                    cursor: 'pointer',
                }}
            />
        );
    }

    return (
        <motion.button
            type="button"
            onClick={toggleTheme}
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.95 }}
            transition={{ duration: ANIMATION.BUTTON_PRESS / 1000 }}
            aria-label={`Switch to ${theme === 'dark' ? 'light' : 'dark'} mode`}
            style={{
                display: 'flex',
                alignItems: 'center',
                justifyContent: 'center',
                width: '44px',
                height: '44px',
                padding: '10px',
                background: 'transparent',
                border: '1px solid var(--color-border)',
                borderRadius: 'var(--border-radius)',
                cursor: 'pointer',
                color: 'var(--color-text-muted)',
                transition: `background-color ${ANIMATION.THEME_TRANSITION}ms var(--ease-smooth)`,
            }}
        >
            {/* Sun icon (shown in dark mode) */}
            {theme === 'dark' ? (
                <svg
                    width="20"
                    height="20"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                >
                    <circle cx="12" cy="12" r="5" />
                    <line x1="12" y1="1" x2="12" y2="3" />
                    <line x1="12" y1="21" x2="12" y2="23" />
                    <line x1="4.22" y1="4.22" x2="5.64" y2="5.64" />
                    <line x1="18.36" y1="18.36" x2="19.78" y2="19.78" />
                    <line x1="1" y1="12" x2="3" y2="12" />
                    <line x1="21" y1="12" x2="23" y2="12" />
                    <line x1="4.22" y1="19.78" x2="5.64" y2="18.36" />
                    <line x1="18.36" y1="5.64" x2="19.78" y2="4.22" />
                </svg>
            ) : (
                /* Moon icon (shown in light mode) */
                <svg
                    width="20"
                    height="20"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                    strokeLinecap="round"
                    strokeLinejoin="round"
                >
                    <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z" />
                </svg>
            )}
        </motion.button>
    );
}
